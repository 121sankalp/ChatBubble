package com.example.chatauda.adaptor;

import android.app.AlertDialog;
import android.content.Context;
import android.content.DialogInterface;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import androidx.annotation.NonNull;
import androidx.recyclerview.widget.RecyclerView;

import com.example.chatauda.R;
import com.example.chatauda.model.messagemodel;
import com.google.firebase.auth.FirebaseAuth;
import com.google.firebase.database.FirebaseDatabase;

import java.util.ArrayList;

public class chatadapter  extends RecyclerView.Adapter{
    ArrayList<messagemodel>messagemodels ;
    Context context ;
    String recid ;
    int SENDER_VIEW_TY= 1 ;
    int RECEIVER_VIEW_TY =2 ;

    public chatadapter(ArrayList<messagemodel> messagemodels, Context context) {
        this.messagemodels = messagemodels;
        this.context = context;
    }

    public chatadapter(ArrayList<messagemodel> messagemodels, Context context, String recid) {
        this.messagemodels = messagemodels;
        this.context = context;
        this.recid = recid;
    }

    @NonNull
    @Override
    public RecyclerView.ViewHolder onCreateViewHolder(@NonNull ViewGroup parent, int viewType){
        if(viewType==SENDER_VIEW_TY)
        {
            View view = LayoutInflater.from(context).inflate(R.layout.sample_sender,parent,false) ;
            return  new SenderViewHolder(view) ;
        }
        else {

            View view = LayoutInflater.from(context).inflate(R.layout.sample_reciver,parent,false) ;
            return  new ReciverViewHolder(view)  ;

        }
    }

    @Override
    public int getItemViewType(int position) {
      if(messagemodels.get(position).getuId().equals(FirebaseAuth.getInstance().getUid()))
        {
            return  SENDER_VIEW_TY ;
        }
        else
        {
            return RECEIVER_VIEW_TY;

        }


    }

    @Override
    public void onBindViewHolder(@NonNull RecyclerView.ViewHolder holder, int position) {


        messagemodel messagemodel=  messagemodels.get(position);
        holder.itemView.setOnLongClickListener(new View.OnLongClickListener() {
            @Override
            public boolean onLongClick(View view) {
                new AlertDialog.Builder(context)
                        .setTitle("Delete")
                        .setMessage("Are you sure you want to delete this  message ?")
                        .setPositiveButton("yes", new DialogInterface.OnClickListener() {
                            @Override
                            public void onClick(DialogInterface dialogInterface, int i) {
                                FirebaseDatabase database = FirebaseDatabase.getInstance();
                                String sender = FirebaseAuth.getInstance().getUid()+ recid ;
                                database.getReference().child("chat") .child(sender)
                                        .child(messagemodel .getMessageid())
                                        .setValue(null );

                            }
                        }) . setNegativeButton("no", new DialogInterface.OnClickListener() {
                            @Override
                            public void onClick(DialogInterface dialogInterface, int i) {
                                dialogInterface.dismiss();
                            }
                        }) .show() ;



                return false;
            }
        });
        if(holder.getClass()== SenderViewHolder.class)
        {
            ((SenderViewHolder)holder).senderMsg.setText(messagemodel.getMessage());

        }
        else
        {
            ((ReciverViewHolder)holder).receiverMsg.setText(messagemodel.getMessage());
        }

    }

    @Override
    public int getItemCount() {
        return messagemodels.size() ;
    }

    public class ReciverViewHolder extends  RecyclerView.ViewHolder {
        TextView receiverMsg, receiverTime ;
        public ReciverViewHolder(@NonNull View itemView) {
            super(itemView);
            receiverMsg=itemView.findViewById(R.id.recivertext);
            receiverTime=itemView.findViewById(R.id.recivertime) ;
        }
    }
    public class SenderViewHolder extends  RecyclerView.ViewHolder {

        TextView senderMsg, senderTime ;
        public SenderViewHolder(@NonNull View itemView) {
            super(itemView);
            senderMsg=itemView.findViewById(R.id.sendertext);
            senderTime=itemView.findViewById(R.id.sendertime) ;
        }
    }


}



















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































